class Employee {
  String id;
  String firstName;
  String lastName;
  String identification;
  String email;
  String profilePicture;
  String position;
  bool isManager;

  Employee({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.identification,
    required this.email,
    required this.profilePicture,
    required this.position,
    required this.isManager,
  });
}
